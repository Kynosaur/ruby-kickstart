def pathify(dir=Hash.new)
    return dir.map{|i| "/#{i}"} if dir.is_a? Array
    to_return = []
    dir.each do |k, v|
        working_directory = "/#{k}"
        p working_directory
        paths = pathify(v)
        paths.each do |path|
            to_return << (working_directory + path)
        end
    end
    to_return
end